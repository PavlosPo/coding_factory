package gr.aueb.cf.ch15.homework.Exercise1;

public class Point {
    private int x;

    public Point() {}

    public Point(int x) {
        this.x = x;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public double getDistanceFromOrigin() {
        return x;
    }
}
