package gr.aueb.cf.ch3;

/**
 * Εκτελεί ένα συγκεκριμένο αριθμό
 * επαναλήψεων που ορίζεται από την αρχική τιμή
 * την τελική τιμή και το step
 */
public class CounterApp {

    public static void main(String[] args) {
        int i = 1;

        while (i <= 10){
            System.out.println("*");
            i++;
        }
    }
}
